/*
Вводим число от 1 до 4. Выводим направление: 1-направо, 2-вверх, 3-вправо, 4-вниз
Плюс горизонтальное и вертикальное движение.
*/

#include <iostream>
using namespace std;

int main()
{
    setlocale(LC_ALL, "Russian");
    int x;
    cin >> x;
    switch (x) {
    case 1 :
        cout << "направо" << endl << "горизонтально";
        break;
    case 2:
        cout << "вверх" << endl << "вертикально";
        break;
    case 3:
        cout << "вправо" << endl << "горизонтально";
        break;
    case 4:
        cout << "вниз" << endl << "вертикально";
        break;
    }
}
